        /// <summary>
        /// MPEG-2 transport stream.
        /// </summary>
        public static readonly Guid MPEG2Program = new Guid(0x263067d1, 0xd330, 0x45dc, 0xb6, 0x69, 0x34, 0xd9, 0x86, 0xe4, 0xe3, 0xe1);
    }

}
